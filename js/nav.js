function openNav() {
	document.getElementsByClassName("sidenav-background")[0].style.width = "100%";
    document.getElementsByClassName("sidenav")[0].style.width = "250px";
}

function closeNav() {
	document.getElementsByClassName("sidenav-background")[0].style.width = "0";
    document.getElementsByClassName("sidenav")[0].style.width = "0";
}